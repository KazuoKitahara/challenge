def halfint(x):
    """
    Returns x/2.
    :param x: int.
    :returns int half of x.
    """
    return int(x / 2)

def fourt(x):
    """
    Returns x * 4.
    :param x: int.
    :returns int product of x and 4.
    """
    return int(x * 4)

result=halfint(100)
print(result)
print(fourt(result))
